package Practica3;

public class AreaEmpresa{
	private String rep;
	private int retardo;
	
	public AreaEmpresa() {
		this.rep = "representacion";
		this.retardo = 0;
	}

	public String getRep() {
		return rep;
	}

	public void setRep(String rep) {
		this.rep = rep;
	}

	public int getRetardo() {
		return retardo;
	}

	public void setRetardo(int retardo) {
		this.retardo = retardo;
	}

}